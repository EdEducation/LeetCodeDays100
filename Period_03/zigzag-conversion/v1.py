class Solution:
    def convert(self, s: str, numRows: int) -> str:
        """
        Преобразует строку в zigzag паттерн и читает построчно.
        
        Алгоритм:
        - Если numRows == 1, возвращаем исходную строку
        - Создаем список строк для каждой "строки" зигзага
        - Идем по символам исходной строки, двигаясь вниз, затем вверх по диагонали
        - Добавляем каждый символ в соответствующую строку
        - Объединяем все строки в результат
        
        Временная сложность: O(n)
        Пространственная сложность: O(n)
        """
        # Базовый случай: если всего 1 строка, зигзага нет
        if numRows == 1 or numRows >= len(s):
            return s
        
        # Создаем список строк для каждой "корзины" (строки зигзага)
        rows = [''] * numRows
        
        current_row = 0  # текущая строка, куда кладем символ
        going_down = False  # направление движения
        
        # Проходим по каждому символу строки
        for char in s:
            # Кладем символ в текущую строку
            rows[current_row] += char
            
            # Если мы на первой или последней строке, меняем направление
            if current_row == 0 or current_row == numRows - 1:
                going_down = not going_down
            
            # Двигаемся вверх или вниз
            current_row += 1 if going_down else -1
        
        # Объединяем все строки в результат
        return ''.join(rows)


