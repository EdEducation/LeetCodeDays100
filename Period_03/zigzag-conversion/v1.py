    # Тест 7: Один символ, несколько строк
    assert solution.convert("X", 10) == "X", \
        "Тест 7 провален: один символ, много строк"
    
    # Тест 8: Строка из 2 символов, 2 строки
    assert solution.convert("AB", 2) == "AB", \
        "Тест 8 провален: два символа, две строки"
    
