from typing import List

class Solution:
    def solveSudoku(self, board: List[List[str]]) -> None:
        """
        Do not return anything, modify board in-place instead.
        """
        # Запускаем рекурсивный поиск с возвратом
        self.solve(board)
    
    def solve(self, board: List[List[str]]) -> bool:
        """Рекурсивная функция поиска с возвратом"""
        # Находим первую пустую клетку
        for row in range(9):
            for col in range(9):
                if board[row][col] == '.':
                    # Пробуем поставить цифры от 1 до 9
                    for num in range(1, 10):
                        if self.is_valid(board, row, col, str(num)):
                            # Ставим цифру
                            board[row][col] = str(num)
                            
                            # Рекурсивно пробуем решить дальше
                            if self.solve(board):
                                return True
                            
                            # Если не получилось - откатываем (backtrack)
                            board[row][col] = '.'
                    
                    # Если ни одна цифра не подошла - возвращаем False
                    return False
        
        # Если пустых клеток нет - судоку решена
        return True
    
    def is_valid(self, board: List[List[str]], row: int, col: int, num: str) -> bool:
        """Проверяет, можно ли поставить число num в клетку (row, col)"""
        
        # Проверяем строку
        for j in range(9):
            if board[row][j] == num:
                return False
        
        # Проверяем столбец
        for i in range(9):
            if board[i][col] == num:
                return False
        
        # Проверяем квадрат 3x3
        box_row = (row // 3) * 3
        box_col = (col // 3) * 3
        for i in range(box_row, box_row + 3):
            for j in range(box_col, box_col + 3):
                if board[i][j] == num:
                    return False
        
        return True


"""
Лучшие известные алгоритмы для решения судоку:

1. Backtracking (поиск с возвратом) — классический и надёжный метод.
   Временная сложность в худшем случае O(9^(m)), где m — количество пустых клеток.
   Оптимизируется с помощью "forward checking" и выбором клетки с минимальным
   количеством возможных вариантов (MRV — minimum remaining values heuristic).

2. Точное покрытие (Exact Cover) — алгоритм Кнута (Dancing Links, DLX).
   Позволяет решать судоку за O(1) для стандартной сетки 9x9 с точки зрения
   теоретической сложности (константное число операций, так как размер фиксирован).
   На практике — один из самых быстрых методов.

3. Алгоритм Крокера (Crook's algorithm) — использует человеческую логику
   (скрытые и открытые одиночки, пары, тройки и т.д.) + поиск с возвратом.
   Часто используется в обучающих целях.

Для больших судоку (например, 16x16) рекомендуются Dancing Links.
Для стандартного 9x9 классического бэктрекинга с эвристиками достаточно.

Однако, так как размер сетки фиксирован (9x9), любой корректный алгоритм
выполняется за O(1) по времени (константное количество итераций, не зависящее
от входных данных, потому что 81 клетка — константа).
"""


