from typing import List

class Solution:
    def solveSudoku(self, board: List[List[str]]) -> None:
        """
        Решение судоку с backtracking и битовыми масками для O(1) проверки
        """
        # Инициализация битовых масок
        # Создаём 9 целых чисел для строк (каждое число хранит 9 битов - для цифр 1-9)
        # 0 в двоичной системе = 000000000 (ни одна цифра не использована)
        self.rows = [0] * 9      # 9 строк
        
        # Создаём 9 целых чисел для столбцов
        # Аналогично строкам, каждый столбец хранит битовую маску использованных цифр
        self.cols = [0] * 9      # 9 столбцов
        
        # Создаём 9 целых чисел для блоков 3x3
        # Индексация блоков: 0 1 2
        #                   3 4 5
        #                   6 7 8
        self.boxes = [0] * 9     # 9 блоков 3x3
        
        # Заполнение масок на основе начальной доски
        # Проходим по всем 81 клеткам (9 строк × 9 столбцов)
        for row in range(9):
            for col in range(9):
                # Проверяем, есть ли в клетке цифра (не пустая ли она)
                if board[row][col] != '.':
                    # Преобразуем строковое представление цифры в целое число
                    # Например: '5' → 5, '3' → 3
                    num = int(board[row][col])
                    # Устанавливаем бит для этой цифры в соответствующих масках
                    self.place_number(row, col, num)
        
        # Запуск рекурсивного поиска
        # Начинаем с первой строки (0) и первого столбца (0)
        # Параметры row и col не используются в нашей реализации (мы используем MRV),
        # но оставлены для совместимости с сигнатурой
        self.solve(board, 0, 0)
    
    def place_number(self, row: int, col: int, num: int) -> None:
        """
        Устанавливает число в маски (бит num-1)
        """
        # Создаём битовую маску для числа
        # Например: num=5 → 1 << 4 = 16 (двоичное: 000010000)
        # Почему num-1? Потому что биты нумеруются с 0:
        # цифра 1 → бит 0 (1 << 0 = 1)
        # цифра 2 → бит 1 (1 << 1 = 2)
        # цифра 9 → бит 8 (1 << 8 = 256)
        bit = 1 << (num - 1)
        
        # Устанавливаем бит в маске строки с помощью побитового ИЛИ (|)
        # Например: было 000010000 (уже есть 5), ставим 000000100 (цифра 3)
        # 000010000 | 000000100 = 000010100 (теперь есть и 5, и 3)
        self.rows[row] |= bit
        
        # Устанавливаем бит в маске столбца
        self.cols[col] |= bit
        
        # Вычисляем индекс блока 3x3
        # row // 3 даёт номер блока по вертикали (0,1,2)
        # col // 3 даёт номер блока по горизонтали (0,1,2)
        # Умножаем row//3 на 3, чтобы получить индекс первого блока в строке
        # Например: row=4, col=5 → row//3=1, col//3=1 → box_idx=1*3+1=4
        box_idx = (row // 3) * 3 + (col // 3)
        
        # Устанавливаем бит в маске блока
        self.boxes[box_idx] |= bit
    
    def remove_number(self, row: int, col: int, num: int) -> None:
        """
        Удаляет число из масок (используется при backtracking)
        """
        # Создаём битовую маску для числа
        bit = 1 << (num - 1)
        
        # Удаляем бит из маски строки с помощью побитового И с инвертированной маской
        # ~bit инвертирует все биты (0→1, 1→0)
        # Например: было 000010100 (цифры 3 и 5), удаляем цифру 3 (000000100)
        # ~bit = 111111011 (все биты 1, кроме бита цифры 3)
        # 000010100 & 111111011 = 000010000 (осталась только цифра 5)
        self.rows[row] &= ~bit
        
        # Удаляем бит из маски столбца
        self.cols[col] &= ~bit
        
        # Вычисляем индекс блока
        box_idx = (row // 3) * 3 + (col // 3)
        
        # Удаляем бит из маски блока
        self.boxes[box_idx] &= ~bit
    
    def is_valid(self, row: int, col: int, num: int) -> bool:
        """
        Проверяет, можно ли поставить число, используя битовые маски O(1)
        """
        # Создаём битовую маску для числа
        bit = 1 << (num - 1)
        
        # Вычисляем индекс блока
        box_idx = (row // 3) * 3 + (col // 3)
        
        # Проверяем, не занят ли бит в любой из трёх масок
        # Оператор & (побитовое И) проверяет, установлен ли бит
        # Если бит установлен хотя бы в одной маске, результат будет != 0
        # Используем not, чтобы вернуть True, если бит нигде не установлен
        # Пример: rows[row] & bit = 0 (нет цифры), cols[col] & bit = 0, boxes[box_idx] & bit = 0
        # (0 or 0 or 0) = 0, not 0 = True → валидно
        return not (self.rows[row] & bit or 
                   self.cols[col] & bit or 
                   self.boxes[box_idx] & bit)
    
    def get_candidates(self, row: int, col: int) -> List[int]:
        """
        Возвращает список возможных чисел для клетки (оптимизация)
        """
        # Объединяем все использованные цифры из строки, столбца и блока
        # Оператор | (побитовое ИЛИ) объединяет установленные биты
        # Например: строка: 000010100 (3 и 5), столбец: 000001010 (2 и 5)
        # блок: 000100010 (4 и 8). Результат: 000111110 (2,3,4,5,8)
        box_idx = (row // 3) * 3 + (col // 3)
        used = self.rows[row] | self.cols[col] | self.boxes[box_idx]
        
        # Список для хранения доступных кандидатов
        candidates = []
        
        # Проверяем все цифры от 1 до 9
        for num in range(1, 10):
            # Создаём бит для текущей цифры
            bit = 1 << (num - 1)
            
            # Если бит НЕ используется (used & bit == 0), добавляем цифру в кандидаты
            # Почему not? Потому что used & bit == 0 → бит свободен
            if not (used & bit):
                candidates.append(num)
        
        return candidates
    
    def find_best_cell(self, board: List[List[str]]) -> tuple:
        """
        Находит клетку с минимальным количеством вариантов (MRV эвристика)
        MRV = Minimum Remaining Values - выбираем клетку, у которой меньше всего возможных чисел
        Это значительно сокращает дерево поиска (ветвление)
        
        Возвращает (row, col, candidates) или (None, None, None) если нет пустых
        """
        best_row = best_col = None          # Лучшие координаты клетки
        best_candidates = None              # Список кандидатов для лучшей клетки
        min_candidates = 10                 # Минимальное количество кандидатов (максимум 9, стартуем с 10)
        
        # Перебираем все 81 клетку доски
        for row in range(9):
            for col in range(9):
                # Если клетка пустая
                if board[row][col] == '.':
                    # Получаем список возможных чисел для этой клетки
                    candidates = self.get_candidates(row, col)
                    
                    # Если у этой клетки кандидатов меньше, чем у текущей лучшей
                    # (первая найденная всегда будет лучше, так как min_candidates=10)
                    if len(candidates) < min_candidates:
                        # Обновляем лучшие значения
                        min_candidates = len(candidates)
                        best_row, best_col = row, col
                        best_candidates = candidates
                        
                        # Оптимизация: если нашли клетку с 1 кандидатом - это идеальный вариант
                        # Дальше искать смысла нет, возвращаем сразу
                        if min_candidates == 1:
                            return best_row, best_col, best_candidates
        
        # Возвращаем лучшую клетку (или None,None,None если пустых клеток нет)
        return best_row, best_col, best_candidates
    
    def solve(self, board: List[List[str]], row: int = 0, col: int = 0) -> bool:
        """
        Рекурсивный поиск с возвратом и MRV эвристикой
        
        Возвращает True, если решение найдено, иначе False
        """
        # Находим лучшую клетку для заполнения (используем MRV эвристику)
        # В отличие от обычного рекурсивного поиска, мы не идём по порядку,
        # а выбираем самую "ограниченную" клетку
        row, col, candidates = self.find_best_cell(board)
        
        # Если row is None - значит пустых клеток больше нет
        # Это означает, что доска полностью заполнена и валидна → решение найдено
        if row is None:
            return True
        
        # Пробуем все возможные числа для выбранной клетки
        # Обычно их 1-9, но с MRV часто 1-2, что сильно ускоряет поиск
        for num in candidates:
            # Проверяем, можно ли поставить это число (хотя мы уже проверили через get_candidates,
            # но из-за многопоточности рекурсии состояние могло измениться)
            if self.is_valid(row, col, num):
                # Ставим число на доску (строковое представление)
                board[row][col] = str(num)
                
                # Обновляем битовые маски (отмечаем, что число использовано)
                self.place_number(row, col, num)
                
                # Рекурсивно пытаемся решить оставшуюся часть судоку
                # Если решение найдено, распространяем True вверх по стеку вызовов
                if self.solve(board):
                    return True
                
                # Если с этим числом решение не найдено (рекурсия вернула False)
                # Откатываем изменения (backtrack):
                # 1. Очищаем клетку на доске
                board[row][col] = '.'
                
                # 2. Удаляем число из битовых масок
                self.remove_number(row, col, num)
                
                # 3. Пробуем следующее число из candidates
        
        # Если перебрали все числа и ни одно не привело к решению
        # Возвращаем False, чтобы вышестоящий уровень рекурсии попробовал другой вариант
        return False


