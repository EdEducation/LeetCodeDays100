"""
Задача: Valid Anagram (Действительная анаграмма)
Проверить, является ли строка t анаграммой строки s.

Ограничения:
- 1 <= длины строк <= 5 * 10^4
- Только строчные латинские буквы

Лучшее решение:
Существует решение за O(n) по времени и O(1) по памяти (если считать алфавит фиксированным).
Но есть и математическое/теоретическое замечание:
Если бы строки могли содержать любые символы Unicode, оптимальное решение - сортировка O(n log n) 
или подсчёт через хеш-таблицу O(n).

Однако для данного ограничения (только строчные латинские буквы, 26 символов):
- Оптимальное решение: массив счётчиков из 26 элементов
- Временная сложность: O(n) где n = длина строк
- Пространственная сложность: O(1) (фиксированный размер 26)

Именованный алгоритм: "Counting Sort based approach" или "Frequency Counter method"

Теоретически, если бы алфавит был фиксированным и очень маленьким (например, ASCII 128),
можно было бы использовать битовые маски/векторные инструкции для O(1) амортизированного времени,
но для строк длиной до 5*10^4 это излишне.
"""

class Solution:
    def isAnagram(self, s: str, t: str) -> bool:
        if len(s) != len(t):
            return False
        
        return sorted(s) == sorted(t)


# Тесты
def run_tests():
    solution = Solution()
    
    # Тест 1: Базовая анаграмма
    assert solution.isAnagram("anagram", "nagaram") == True, "Тест 1 провален"
    
    # Тест 2: Не анаграмма
    assert solution.isAnagram("rat", "car") == False, "Тест 2 провален"
    
    # Тест 3: Пустые строки (не по условию, но для полноты)
    assert solution.isAnagram("", "") == True, "Тест 3 провален"
    
    # Тест 4: Одинаковые строки
    assert solution.isAnagram("abc", "abc") == True, "Тест 4 провален"
    
    # Тест 5: Разная длина
    assert solution.isAnagram("abc", "ab") == False, "Тест 5 провален"
    
    # Тест 6: С повторяющимися символами
    assert solution.isAnagram("aacc", "ccac") == False, "Тест 6 провален"
    
    # Тест 7: Все символы одинаковые
    assert solution.isAnagram("aaaa", "aaaa") == True, "Тест 7 провален"
    
    # Тест 8: Длинные строки с одинаковой частотой
    assert solution.isAnagram("abcdefghijklmnopqrstuvwxyz" * 100, 
                              "zyxwvutsrqponmlkjihgfedcba" * 100) == True, "Тест 8 провален"
    
    # Тест 9: Один символ
    assert solution.isAnagram("a", "a") == True, "Тест 9 провален"
    assert solution.isAnagram("a", "b") == False, "Тест 10 провален"
    
    print("Все тесты пройдены!")


# Запуск тестов
if __name__ == "__main__":
    run_tests()