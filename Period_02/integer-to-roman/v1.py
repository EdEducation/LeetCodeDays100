# Есть известный алгоритм: жадный подход с предопределёнными парами "значение -> символ"
# Так как диапазон входных данных ограничен (1 <= num <= 3999), можно предопределить все возможные комбинации
# и за O(1) собрать результат, последовательно вычитая максимально возможные значения.
# Более того, из-за фиксированного набора символов вместо динамического вычитания можно использовать
# предопределённые значения для всех десятичных разрядов: тысячи, сотни, десятки, единицы.

class Solution:
    def intToRoman(self, num: int) -> str:
        # Реализовать с использованием жадного алгоритма или таблицы соответствий
        # Ожидаемая сложность O(1)
        
        # Предопределённые пары "значение -> символ" в порядке убывания
        # Это позволяет использовать жадный алгоритм: на каждом шаге берём наибольшее возможное значение
        value_to_symbol = [
            (1000, "M"),
            (900, "CM"),
            (500, "D"),
            (400, "CD"),
            (100, "C"),
            (90, "XC"),
            (50, "L"),
            (40, "XL"),
            (10, "X"),
            (9, "IX"),
            (5, "V"),
            (4, "IV"),
            (1, "I")
        ]
        
        result = []
        
        # Жадный алгоритм: последовательно вычитаем максимально возможные значения
        for value, symbol in value_to_symbol:
            while num >= value:
                result.append(symbol)
                num -= value
        
        return ''.join(result)


# Тесты (примеры из условия задачи)
def run_tests():
    solution = Solution()
    
    # Тест 1
    assert solution.intToRoman(3749) == "MMMDCCXLIX", "Тест 1 провален"
    # Тест 2
    assert solution.intToRoman(58) == "LVIII", "Тест 2 провален"
    # Тест 3
    assert solution.intToRoman(1994) == "MCMXCIV", "Тест 3 провален"
    # Дополнительный тест: минимальное значение
    assert solution.intToRoman(1) == "I", "Тест 4 провален"
    # Дополнительный тест: максимальное значение
    assert solution.intToRoman(3999) == "MMMCMXCIX", "Тест 5 провален"
    
    print("Все тесты пройдены!")


if __name__ == "__main__":
    run_tests()