from typing import List

# 2078. Two Furthest Houses With Different Colors
# Сложность: Easy
# Тэги: Array, Greedy
#
# Описание задачи:
# Есть n домов, каждый покрашен в свой цвет (задан массивом colors).
# Нужно найти максимальное расстояние (разницу индексов) между двумя домами с разными цветами.
#
# Примеры:
# Пример 1:
# Вход: colors = [1,1,1,6,1,1,1]
# Выход: 3
# Объяснение: Дом 0 (цвет 1) и дом 3 (цвет 6) дают расстояние 3.
#
# Пример 2:
# Вход: colors = [1,8,3,8,3]
# Выход: 4
# Объяснение: Дом 0 (цвет 1) и дом 4 (цвет 3) дают расстояние 4.
#
# Пример 3:
# Вход: colors = [0,1]
# Выход: 1
#
# Ограничения:
# - n == colors.length
# - 2 <= n <= 100
# - 0 <= colors[i] <= 100
# - Гарантировано хотя бы два дома с разными цветами
#
# Оптимальное решение:
# Жадный подход за O(n). Максимальное расстояние будет достигаться либо с участием
# самого левого дома (индекс 0), либо самого правого дома (индекс n-1).
# Достаточно проверить:
# 1. Для каждого дома i справа налево: если colors[i] != colors[0], обновить ответ = i - 0
# 2. Для каждого дома i слева направо: если colors[i] != colors[n-1], обновить ответ = (n-1) - i
# Это работает за O(n) и O(1) памяти.
# 
# Альтернатива: полный перебор всех пар за O(n^2), но при n <= 100 это тоже допустимо,
# но жадный способ оптимальнее.

class Solution:
    def maxDistance(self, colors: List[int]) -> int:
        """
        Возвращает максимальное расстояние между двумя домами с разными цветами.
        
        :param colors: список цветов домов
        :return: максимальное расстояние (разница индексов)
        """
        n = len(colors)
        # Проверяем от крайних домов (жадный подход)
        # Вариант 1: от левого края
        left_color = colors[0]
        max_dist = 0
        for i in range(n - 1, -1, -1):
            if colors[i] != left_color:
                max_dist = max(max_dist, i - 0)
                break
        
        # Вариант 2: от правого края
        right_color = colors[n - 1]
        for i in range(n):
            if colors[i] != right_color:
                max_dist = max(max_dist, (n - 1) - i)
                break
        
        return max_dist


# Тесты
def run_tests():
    solution = Solution()
    
    # Тест 1: из примера 1
    assert solution.maxDistance([1, 1, 1, 6, 1, 1, 1]) == 3, "Тест 1 провален"
    
    # Тест 2: из примера 2
    assert solution.maxDistance([1, 8, 3, 8, 3]) == 4, "Тест 2 провален"
    
    # Тест 3: из примера 3
