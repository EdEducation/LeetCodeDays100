    # Тест 4: минимальная длина 2, разные цвета
    assert solution.maxDistance([5, 10]) == 1, "Тест 4 провален"
    
    # Тест 5: все одинаковые, кроме последнего
    assert solution.maxDistance([2, 2, 2, 2, 5]) == 4, "Тест 5 провален"
    
    # Тест 6: все одинаковые, кроме первого
    assert solution.maxDistance([7, 1, 1, 1, 1]) == 4, "Тест 6 провален"
    
